package br.com.voxage.botomnilink.models;

public class EnvTitulos {
	private String email;
	private String titulosEnviado;
	private String numOcorrencia;
	
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getTitulosEnviado() {
		return titulosEnviado;
	}
	public void setTitulosEnviado(String titulosEnviado) {
		this.titulosEnviado = titulosEnviado;
	}
	public String getNumOcorrencia() {
		return numOcorrencia;
	}
	public void setNumOcorrencia(String numOcorrencia) {
		this.numOcorrencia = numOcorrencia;
	}
}
