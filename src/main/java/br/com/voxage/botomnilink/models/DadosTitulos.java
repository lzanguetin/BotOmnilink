package br.com.voxage.botomnilink.models;

public class DadosTitulos {
	private double valorAbertos;
	private double valorVencidos;
	private double valorAssessoria;
	private String qtdeAssessoria;
	private String qtdeAbertos;
	private String qtdeVencidos;
	
	public double getValorAbertos() {
		return valorAbertos;
	}
	
	public void setValorAbertos(double valorAbertos) {
		this.valorAbertos = valorAbertos;
	}
	
	public double getValorVencidos() {
		return valorVencidos;
	}
	
	public void setValorVencidos(double valorVencidos) {
		this.valorVencidos = valorVencidos;
	}
	
	public double getValorAssessoria() {
		return valorAssessoria;
	}
	
	public void setValorAssessoria(double valorAssessoria) {
		this.valorAssessoria = valorAssessoria;
	}
	
	public String getQtdeAssessoria() {
		return qtdeAssessoria;
	}
	
	public void setQtdeAssessoria(String qtdeAssessoria) {
		this.qtdeAssessoria = qtdeAssessoria;
	}
	
	public String getQtdeAbertos() {
		return qtdeAbertos;
	}
	
	public void setQtdeAbertos(String qtdeAbertos) {
		this.qtdeAbertos = qtdeAbertos;
	}
	
	public String getQtdeVencidos() {
		return qtdeVencidos;
	}
	
	public void setQtdeVencidos(String qtdeVencidos) {
		this.qtdeVencidos = qtdeVencidos;
	}
}
